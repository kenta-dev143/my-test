    function _makeQuestion($event_id){
        global $_conf_kaitou_type;

        $sql  = "";
        $sql .= " select * from m_question ";
        $sql .= " where ";
        $sql .= "   que_event_id='"._as($event_id)."'";
        $question_rec = _select($sql);

        $ret = array();
        if( _count($question_rec) > 0 ){
            for($i=1; $i<=30; $i++){
                $idx = sprintf('%02d',$i);
                if($question_rec[0]['que_question_'.$idx] != ""){
                    $ret['ans_'.$idx]['no'] = $i;
                    $ret['ans_'.$idx]['question'] = $question_rec[0]['que_question_'.$idx];
                    $ret['ans_'.$idx]['hissu_flg'] = $question_rec[0]['que_hissu_flg_'.$idx];
                    $ret['ans_'.$idx]['type'] = $_conf_kaitou_type[$question_rec[0]['que_keishiki_kbn_'.$idx]];

                    $sentakushi_arr = explode(",",$question_rec[0]['que_sentakushi_'.$idx]);
                    if(_count($sentakushi_arr) > 0){
                        foreach($sentakushi_arr as $val){
                            if($val != ""){
                                $ret['ans_'.$idx]['options'][trim($val)] = trim($val);
                            }
                        }
                    }

                    if($question_rec[0]['que_keishiki_kbn_'.$idx] == '3' || $question_rec[0]['que_keishiki_kbn_'.$idx] == '5'){
                        $ret['ans_sub_'.$idx]['question'] = $question_rec[0]['que_keishiki_comment_'.$idx];
                        $ret['ans_sub_'.$idx]['type'] = "sub_text";
                    }
                }
            }
        }


        return $ret;

    }

