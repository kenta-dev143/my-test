    function _smartyFetchFromMailTplDB(&$_smarty,$w_tpl_name){
        global $conn;

        //.tplの拡張子がついていたら取り除く
        if( substr($w_tpl_name,-4)==".tpl" ){
            $w_tpl_name = substr($w_tpl_name,0,-4);
        }

        //DBからテンプレ情報(subjectとbodyを取得)
        $recs = _select("select * from m_mail_template where mailt_key='".$w_tpl_name."'");
        if( _count($recs)==0 ) die('['.$w_tpl_name.'] mail templates not found in database !');

        //subjectとbodyを区切り文字で繋いでまとめて文字列にし、％タグをSmartyタグに変換
        $all = $recs[0]['mailt_subject']."___###kugiri###___".$recs[0]['mailt_body'];
        $all = str_replace('&lt;%', '<%', $all);
        $all = str_replace('%&gt;', '%>', $all);
        $all = str_replace('<%', '{$', $all);
        $all = str_replace('%>', '}', $all);
        $tpl_source = $all;

        //テンプレ文字列からfetch実行
        $_buff = $_smarty->fetch("eval:".$tpl_source);

        //
        list($subject,$body) = explode("___###kugiri###___", $_buff);
        $ret = array();
        $ret['subject'] = $subject;
        $ret['body'] = $body;

        return $ret;
    }

