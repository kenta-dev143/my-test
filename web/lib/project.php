    function _getMailAddressFromID($id){
        $mailAddress = $id;

        // @の個数チェック
        $atmark_count = mb_substr_count ( $id, '@');
        if ( $atmark_count >= 2 ){
            $pos = strpos( $id, '@');
            $pos = strpos( $id, '@', ($pos+1));
            $mailAddress = substr($id, 0, $pos);
        }

        return $mailAddress;
    }

